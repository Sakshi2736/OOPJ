 class Question_09 {
    public static void main(String[] args) {
        int num1 = 10; // First predefined integer
        int num2 = 5;  // Second predefined integer

        System.out.println(num1 + " == " + num2 + ": " + (num1 == num2));
        System.out.println(num1 + " != " + num2 + ": " + (num1 != num2));
        System.out.println(num1 + " > " + num2 + ": " + (num1 > num2));
        System.out.println(num1 + " < " + num2 + ": " + (num1 < num2));
        System.out.println(num1 + " >= " + num2 + ": " + (num1 >= num2));
        System.out.println(num1 + " <= " + num2 + ": " + (num1 <= num2));
    }
}
